class Variable {
    public static void main(String[] args) {
        String str = "hello"

        println(str)

        // Defining a variable in lowercase
        int x = 5;
        // Defining a variable in uppercase
        int X = 6;
        // Defining a variable with the underscore in it's name
        def _Name = "Joe";

        println(x);
        println(X);
        println(_Name);
    }
}
